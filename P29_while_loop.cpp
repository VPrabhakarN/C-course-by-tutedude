// C++ program to demonstrate 'while loop'

#include<iostream>

using namespace std;

int main()
{
    cout <<"It is an example of while loop" << endl;
    int a;
    a = 0;

    // using while loop to print 0-9 numbers
    while (a < 10){
        cout << a << endl;
        a++;
    }

    return 0;
}